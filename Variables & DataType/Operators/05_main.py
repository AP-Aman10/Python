# Membership Operators
print("====*==== Membership Operators ====+====")
nums = [2, 4, 10, 20, 45, 47, 64, 81, 96, 100]
print(f"Num: {nums}")
print(f"20 in nums      : {20 in nums}")
print(f"49 in nums      : {49 in nums}")
print()

print(f"50 not in nums  : {50 not in nums}")
print(f"64 not in nums  : {64 not in nums}")
print()