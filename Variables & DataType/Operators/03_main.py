num1 = True
num2 = False

# Logical Operators [And]
print("\n====+==== Logical Operators [And] ====+====")
print(f"{num1} And {num1} = {num1 and num1}")
print(f"{num2} And {num2} = {num2 and num2}")
print(f"{num1} And {num2} = {num1 and num2}")
print(f"{num2} And {num1} = {num2 and num1}")

# Logical Operators [Or]
print("\n====+==== Logical Operators [Or] ====+====")
print(f"{num1} Or {num1} = {num1 or num1}")
print(f"{num2} Or {num2} = {num2 or num2}")
print(f"{num1} Or {num2} = {num1 or num2}")
print(f"{num2} Or {num1} = {num2 or num1}")

# Logical Operators [Not]
print("\n====+==== Logical Operators [Or] ====+====")
print(f"Not {num1} = {not(num1)}")
print(f"Not {num2} = {not(num2)}")