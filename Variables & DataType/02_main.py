# Integer
Num = 19
print(f"Integer: {Num}")
print(f"Type of Num: {type(Num)}")
print("====*==== Integer ====+====\n")

# Float
Num2 = 96.45
print(f"Float: {Num2}")
print(f"Type of Num2: {type(Num2)}")
print("====*==== Float ====+====\n")

# String
name = "Aman Patidar"
print(f"String: {name}")
print(f"Type of name: {type(name)}")
print("====*==== String ====+====\n")

# Boolean
is_active = True
print(f"Boolean: {is_active}")
print(f"Type of is_active: {type(is_active)}")
print("====*==== Boolean ====+====\n")

# List
fruits = ["apple", "banana", "cherry"]
print(f"List: {fruits}")
print(f"Type of fruits: {type(fruits)}")
print("====*==== List ====+====\n")

# Tuple
coordinates = (10.5, 20.3)
print(f"Tuple: {coordinates}")
print(f"Type of coordinates: {type(coordinates)}")
print("====*==== Tuple ====+====\n")

# Set
unique_numbers = {1, 2, 3, 3, 2}
print(f"Set: {unique_numbers}")
print(f"Type of unique_numbers: {type(unique_numbers)}")
print("====*==== Set ====+====\n")

# Dictionary
person = {"name": "Aman", "age": 25, "city": "Indore"}
print(f"Dictionary: {person}")
print(f"Type of person: {type(person)}")
print("====*==== Dictionary ====+====\n")

# NoneType
nothing = None
print(f"NoneType: {nothing}")
print(f"Type of nothing: {type(nothing)}")
print("====*==== NoneType ====+====\n")
