# For loop with a list
numbers_list = [1, 4, 6, 234, 6, 764]
for number in numbers_list:
    print(number)

# For loop with a tuple
numbers_tuple = (6, 231, 75, 122)
for number in numbers_tuple:
    print(number)

# For loop with a string
name_string = "Aman"
for character in name_string:
    print(character)